let quizHistory = [];

function loadHistory() {
    try {
        const saved = localStorage.getItem('quizHistory');
        if (saved) {
            quizHistory = JSON.parse(saved);
        }
    } catch (e) {
        console.error('Greška pri učitavanju povijesti:', e);
        quizHistory = [];
    }
}
function saveQuizResult(category, score, correct, total, accuracy) {
    const result = {
        date: new Date().toISOString(),
        category: category,
        score: score,
        correct: correct,
        total: total,
        accuracy: accuracy,
        categoryName: getCategoryName(category)
    };
    
    quizHistory.unshift(result);
    
    if (quizHistory.length > 50) {
        quizHistory = quizHistory.slice(0, 50);
    }
    
    try {
        localStorage.setItem('quizHistory', JSON.stringify(quizHistory));
    } catch (e) {
        console.error('Greška pri spremanju povijesti:', e);
    }
}

function getCategoryName(category) {
    const names = {
        'znanost': 'Znanost',
        'povijest': 'Povijest',
        'geografija': 'Geografija',
        'opce': 'Opća kultura'
    };
    return names[category] || category;
}

function getStatistics() {
    if (!quizHistory || quizHistory.length === 0) {
        return {
            totalQuizzes: 0,
            averageScore: 0,
            averageAccuracy: 0,
            bestScore: 0,
            favoriteCategory: '-',
            totalQuestions: 0,
            totalCorrect: 0
        };
    }
    
    const totalQuizzes = quizHistory.length;
    const totalScore = quizHistory.reduce((sum, q) => sum + (q.score || 0), 0);
    const totalAccuracy = quizHistory.reduce((sum, q) => sum + (q.accuracy || 0), 0);
    const bestScore = Math.max(...quizHistory.map(q => q.score || 0));
    const totalQuestions = quizHistory.reduce((sum, q) => sum + (q.total || 0), 0);
    const totalCorrect = quizHistory.reduce((sum, q) => sum + (q.correct || 0), 0);
    
    const categoryCounts = {};
    quizHistory.forEach(q => {
        categoryCounts[q.category] = (categoryCounts[q.category] || 0) + 1;
    });
    const favoriteCategory = Object.keys(categoryCounts).reduce((a, b) => 
        categoryCounts[a] > categoryCounts[b] ? a : b
    );
    
    return {
        totalQuizzes,
        averageScore: Math.round(totalScore / totalQuizzes),
        averageAccuracy: Math.round(totalAccuracy / totalQuizzes),
        bestScore,
        favoriteCategory: getCategoryName(favoriteCategory),
        totalQuestions,
        totalCorrect
    };
}

