function loadQuestion() {
    answered = false;
    timeLeft = 30;
    const question = currentQuestions[currentQuestionIndex];
    
    document.getElementById('current-question-num').textContent = currentQuestionIndex + 1;
    document.getElementById('total-questions').textContent = currentQuestions.length;
    document.getElementById('question-text').textContent = question.question;
    document.getElementById('score-display').textContent = score;
    document.getElementById('next-btn').classList.add('hidden');
    
    const timerBar = document.getElementById('timer-bar');
    timerBar.style.width = '100%';
    timerBar.classList.remove('bg-red-500');
    timerBar.classList.add('bg-gradient-to-r', 'from-purple-500', 'to-pink-500');
    
    const optionsContainer = document.getElementById('options-container');
    optionsContainer.innerHTML = '';
    
    const letters = ['A', 'B', 'C', 'D'];
    question.options.forEach((option, index) => {
        const btn = document.createElement('button');
        btn.className = 'option-btn w-full text-left p-4 rounded-xl border-2 border-gray-200 bg-white hover:border-purple-300 font-medium text-gray-700 flex items-center gap-3';
        btn.innerHTML = `
            <span class="w-8 h-8 rounded-full bg-gray-100 text-gray-600 font-bold flex items-center justify-center text-sm">${letters[index]}</span>
            <span>${option}</span>
        `;
        btn.onclick = () => selectAnswer(index, btn);
        optionsContainer.appendChild(btn);
    });
    
    startTimer();
}

function startTimer() {
    clearInterval(timer);
    const timerText = document.getElementById('timer-text');
    const timerBar = document.getElementById('timer-bar');
    
    timer = setInterval(() => {
        timeLeft--;
        timerText.textContent = timeLeft + 's';
        const percentage = (timeLeft / 30) * 100;
        timerBar.style.width = percentage + '%';
        
        if (timeLeft <= 10) {
            timerBar.classList.remove('from-purple-500', 'to-pink-500');
            timerBar.classList.add('bg-red-500');
        }
        
        if (timeLeft <= 0) {
            clearInterval(timer);
            handleTimeout();
        }
    }, 1000);
}

function handleTimeout() {
    if (!answered) {
        answered = true;
        const question = currentQuestions[currentQuestionIndex];
        const buttons = document.querySelectorAll('.option-btn');
        
        buttons[question.correct].classList.add('correct');
        
        document.getElementById('next-btn').classList.remove('hidden');
    }
}

function selectAnswer(selectedIndex, selectedBtn) {
    if (answered) return;
    answered = true;
    clearInterval(timer);
    
    const question = currentQuestions[currentQuestionIndex];
    const buttons = document.querySelectorAll('.option-btn');
    
    buttons.forEach(btn => {
        btn.disabled = true;
        btn.classList.add('cursor-not-allowed', 'opacity-70');
    });
    
    if (selectedIndex === question.correct) {
        selectedBtn.classList.add('correct');
        selectedBtn.classList.remove('opacity-70');
        correctCount++;
        const timeBonus = timeLeft * 2;
        score += (100 + timeBonus);
        playSound('correct');
    } else {
        selectedBtn.classList.add('wrong');
        selectedBtn.classList.remove('opacity-70');
        buttons[question.correct].classList.add('correct');
        buttons[question.correct].classList.remove('opacity-70');
        playSound('wrong');
    }
    
    const scoreDisplay = document.getElementById('score-display');
    scoreDisplay.textContent = score;
    scoreDisplay.classList.add('pulse');
    setTimeout(() => scoreDisplay.classList.remove('pulse'), 500);
    
    document.getElementById('next-btn').classList.remove('hidden');
}

function nextQuestion() {
    currentQuestionIndex++;
    
    if (currentQuestionIndex < currentQuestions.length) {
        loadQuestion();
    } else {
        showResults();
    }
}

